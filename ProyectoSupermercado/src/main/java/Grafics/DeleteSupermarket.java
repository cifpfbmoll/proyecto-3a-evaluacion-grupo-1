    public void populateComboBox() {
        ArrayList info = Supermercado.getSupermarketsFromDatabase();
        String[] valuesToInsert = new String[info.size()];
        for (int i = 0; i < info.size(); i++) {
            valuesToInsert[i] = (info.get(i)).toString();
        }
        
        supermarketComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(valuesToInsert));
    }
    
