    public void obtenerStock(javax.swing.JPanel panel,String tipoProducto) throws SQLException{
        ArrayList <StockProducto> stock=Main.getSupermercadoActivo().getStockSupermercado();
        PreparedStatement query=null;
        ResultSet resultado=null;
        try{
            for (int i=0; i<stock.size(); i++){
                query=Herramientas.getConexion().prepareStatement("SELECT * FROM producto WHERE codigo_producto=? AND tipo_producto=?");
                query.setInt(1,stock.get(i).getCodigoProducto());
                query.setString(2, tipoProducto);
                resultado=query.executeQuery();
                if (resultado.next()) {
                    this.crearLineaCompra(panel,resultado.getString("nombre_producto"),
                    resultado.getDouble("precio_producto"),resultado.getString("descripcionProd"),
                    stock.get(i).getCantidad(), resultado.getInt("codigo_producto"));
                }
            }
        }
        catch(SQLException ex){
            Herramientas.aviso("Se ha producido un error al cargar el stock");
            ex.printStackTrace();
        }
        finally{
            resultado.close();
            query.close();
        }
    }
    
